    }

    /**
     * @return \yii\db\ActiveQuery
     */
    public function getChessMasters()
    {
        return $this->getUserChessRs(['type' => FeUserType::TYPE_MASTER, 'status' => EntityModelStatus::STATUS_ACTIVE])
            ->orderBy(['created_at' => SORT_ASC]);
    }

    /**
     * @return \yii\db\ActiveQuery
