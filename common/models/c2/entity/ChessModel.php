    public function setToFinish()
    {
        // setp1
        $lord = $this->getChessLord()->one();
        $lord->updateAttributes([
            'state' => UserChessRsState::TYPE_FINISH
        ]);
    }

    public function loadItems()
    {
        $this->lordItems = $this->getChessLord()->all();
        $this->elderItems = $this->getChessElder()->all();
        $this->chieftainItems = $this->getChessChieftain()->all();
    }

    public function afterSave($insert, $changedAttributes)
    {
        parent::afterSave($insert, $changedAttributes); // TODO: Change the autogenerated stub
        if ($insert) {
            $rs1 = new LordElderRsModel();
            $rs1->setAttributes([
                'chess_id' => $this->id,
                'lord_id' => $this->lord_id,
                'elder_id' => $this->elder_id,
            ]);
            $rs1->save();
            $rs2 = new ElderChieftainRsModel();
            $rs2->setAttributes([
                'chess_id' => $this->id,
                'elder_id' => $this->elder_id,
                'chieftain_id' => $this->chieftain_id,
            ]);
            $rs2->save();
        } else {
            if (isset($changedAttributes['lord_id'])) {
                $model = LordElderRsModel::findOne(['chess_id' => $this->id]);
                $model->updateAttributes([
                    'lord_id' => $this->lord_id
                ]);
            }
        }
    }

